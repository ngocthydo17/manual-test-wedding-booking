package com.example.Model;

public class BookedDate {
    private String date;
    private String timework;

    public BookedDate(String date, String timework) {
        this.date = date;
        this.timework = timework;
    }

    public String getDate() {
        return date;
    }

    public String getTimework() {
        return timework;
    }

}
